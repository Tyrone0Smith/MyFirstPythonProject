# This is a practice file for using f-strings in Python.
def format_string(name, age, city):
    """
    Formats a string using f-strings to include the name, age, and city.

    Args:
        name (str): The name of the person.
        age (int): The age of the person.
        city (str): The city where the person lives.

    Returns:
        str: A formatted string with the person's details.
    """
    return f"My name is {name}, I am {age} years old, and I live in {city}."
def main():
    # Example usage of the format_string function
    name = "Alice"
    age = 30
    city = "New York"
    
    formatted_string = format_string(name, age, city)
    print(formatted_string)
if __name__ == "__main__":
    main()
